
public class LoopExamples {

    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            System.out.println("i = " + i + " Hello with a for loop");
        }
        int i = 1;
        while (i <= 10) {
            System.out.println("i = " + i + " Hello with a while loop");
            i++;
        }

    }

}
